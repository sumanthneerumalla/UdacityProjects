import turtle
import random

def drawScreen():
    window = turtle.Screen()
    window.bgcolor("red")
    window.exitonclick()

def drawSquare():
    brad = turtle.Turtle()
    brad.shape("classic")
    brad.color("black")
    brad.speed(2)

    for i in range(0,4):
        brad.forward(100)
        brad.right(90)



def drawCircle():
    angie = turtle.Turtle
    angie.shape("turtle")
    angie.color("white")
    angie.setx(100)
    angie.setx(100)
    angie.circle(100)

drawScreen()
drawSquare()
drawCircle()







drawSquare()