import turtle
import random

def drawScreen():
    window = turtle.Screen()
    window.bgcolor("red")
    window.exitonclick()

    brad = turtle.Turtle()
    brad.shape("classic")
    brad.color("black")
    brad.speed(2)

    for i in range(0,4):
        brad.forward(100)
        brad.right(90)



    angie = turtle.Turtle
    angie.shape("turtle")
    angie.color("white")

    angie.circle(100)

drawScreen()